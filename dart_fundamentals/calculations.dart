class Calculations{
  static int add(int a,int b){
    int sum = a+b;
    print("add - $sum");
    return sum;
  }
  static int difference(int a,int b){
    int diff = a-b;
    print("difference - $diff");
    return diff;
  }
  static int multiplies(int a,int b){
    int mul = a*b;
    print("multiplies - $mul");
    return mul;
  }
  static double division(int a,int b){
    double division = a/b;
    print("division - $division");
    return division;
  }
}